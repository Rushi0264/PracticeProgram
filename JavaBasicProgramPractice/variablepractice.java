package JavaBasicProgramPractice;

public class variablepractice {
    //variable declare
    int a=10;
    char b='P';
    double d=8.56;
    float f= 3.6f;
    boolean g=true;
    byte h=90;
    short i=9999;
    long l=1234567890;

    public static void main(String[] args) {
        variablepractice obj = new variablepractice();
        System.out.println("This is value of a: "+obj.a);
        System.out.println("This is value of b: "+obj.b);
        System.out.println("This is value of d: "+obj.d);
        System.out.println("This is value of f: "+obj.f);
        System.out.println("This is value of g: "+obj.g);
        System.out.println("This is value of h: "+obj.h);
        System.out.println("This is value of i: "+obj.i);
        System.out.println("This is value of l: "+obj.l);
    }

}
